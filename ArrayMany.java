import java.util.*;
public class ArrayMany {
    public static void main(String args[]){
       // int[] marks = new int [3]; we can also write it as this 
        int marks[] = new int[3];

        marks[0] = 78;
        marks[1] = 55;
        marks[2] = 34;

        for(int i=0;i<3;i++){
            System.out.println(marks[i]);
        }

    }
    
}
